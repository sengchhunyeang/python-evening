class Employee:
    def __init__(self, emp_id, emp_name, emp_age, emp_city):
        self.emp_id = emp_id
        self.emp_name = emp_name
        self.emp_age = emp_age
        self.emp_city = emp_city

    def __str__(self):
        return f"Id :{self.emp_id}\t name :{self.emp_name}\t age :{self.emp_age}\t city{self.emp_city}"

