# Exercise 2: Positive, Negative, or Zero
# Write a program that asks the user for a number and prints whether it is positive, negative, or zero.

# Example:
# python
num = float(input("Enter a number: "))
if num > 0:
    print("Positive")
elif num < 0:
    print("Negative")
else:
    print("Zero")
# positive and negative
# positive 1 to 1000000000000000000
# negative -1 to -100000000000000000

